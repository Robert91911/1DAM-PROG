package circulo;

public class Circulo {
	
	private double radio;
	
	public Circulo() {
		radio = 2;
	}
//////////////////////////////////////////////////////////////////////////////
	public Circulo(double radio) {
		this.radio = radio;
	}
//////////////////////////////////////////////////////////////////////////////
	public double getRadio() {
		return radio;
	}
//////////////////////////////////////////////////////////////////////////////
	public void setRadio(double radio) {
		this.radio = radio;
	}
//////////////////////////////////////////////////////////////////////////////
	public double area() {
		return (Math.pow(radio, 2)) * Math.PI;
	}
	//////////////////////////////////////////////////
	public double perimetro() {
		return Math.PI * 2 * radio;		
	}
}